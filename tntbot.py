@dataclass
class MemberRecord:
    display_name: str
    last_joined: Optional[datetime] = field(default=None)
    duration: timedelta = field(default_factory=timedelta)

    def on_join(self):
        self.last_joined = datetime.now(TZ)

    def on_leave(self):
        if self.last_joined:
            self.duration += datetime.now(TZ) - self.last_joined
            self.last_joined = None

    # when we want an updated duration but watching hasn't stopped
    def snapshot(self):
        if self.last_joined is not None:
            self.on_leave()
            self.on_join()

# WATCHER - watch voice channels to take attendance
#==============================================================================
@dataclass
class WatchJob:
    member_id: int # needed to dm members
    created_by: str
    name: str
    channel_ids: set[int]
    start: datetime
    end: datetime
    report_to: int # report outputs to this channel
    attendance: dict[int, dict[int, MemberRecord]] = field(default_factory=dict)
    status: str = "waiting"
    cav_only: bool = False
    outages: list[tuple[datetime, datetime]] = field(default_factory=list)
    disconnected_at: Optional[datetime] = field(default=None)

    def __post_init__(self):
        for channel_id in self.channel_ids:
            self.attendance[channel_id] = {}

    def valid_member(self, member):
        # Ignore bots
        if member.bot:
            return None

        # Ignore non-cav and retired members
        if self.cav_only:
            # Example: 'ABC.Name.XYZ Cadre' matches
            match = re.match(r'^([0-9A-Z]{1,3})\.([0-9a-zA-Z,]+)\.([0-9A-Z]{1,3}) (.+)$', member.display_name)
            if not match:
                return None
            else:
                if 'RET' in match.group(4):
                    return None
        return member

    def record_join(self, channel_id: int, member: discord.Member):
        if not self.valid_member(member):
            return
        if member.id not in self.attendance[channel_id]:
            # If member is not being recorded, start new record
            record = MemberRecord(
                display_name=member.display_name,
                last_joined=datetime.now(TZ),
                duration=timedelta(),
            )
            self.attendance[channel_id][member.id] = record
        else:
            record = self.attendance[channel_id][member.id]
            # snapshot because members could join while already joined because the bot was disconnected.
            record.snapshot()

    def record_leave(self, channel_id: int, member: discord.Member):
        if not self.valid_member(member):
            return
        if member.id in self.attendance[channel_id]:
            self.attendance[channel_id][member.id].on_leave()

    def merge_attendance(self) -> dict[int, tuple[str, timedelta]]:
        merged: dict[int, tuple[str, timedelta]] = {}
        for channel_id, members in self.attendance.items():
            for member_id, record in members.items():
                if member_id in merged:
                    name, total = merged[member_id]
                    merged[member_id] = (name, total + record.duration)
                else:
                    merged[member_id] = (record.display_name, record.duration)
        return merged

    def build_report(self):
        now = datetime.now(TZ)

        # Get a snapshot of the attendance
        for members in self.attendance.values():
            for record in members.values():
                record.snapshot()

        # Merge member durations across all watched channels
        merged = self.merge_attendance()

        # Build output string
        lines = []

        # Report outage information
        if self.outages:
            total_downtime = sum((end - start for start, end in self.outages), timedelta())
            lines.append(f"Bot disconnected {len(self.outages)} time(s) while this task was running:")
            lines.append("```")
            for start, end in self.outages:
                duration = end - start
                lines.append(f"{start.strftime('%H:%M:%S')} to {end.strftime('%H:%M:%S')} ({fmt_timedelta(duration)})")
            lines.append(f"Total downtime: {fmt_timedelta(total_downtime)}")
            lines.append("```")

        # Added users in a nice format to be copied pasted.
        if merged:
            lines.append("```")
            for member_id, (display_name, total_duration) in merged.items():
                lines.append(f"{display_name};{fmt_timedelta(total_duration)}")
            lines.append("```")
        else:
            lines.append("No members recorded.")

        return "\n".join(lines)

