    @commands.command()
    async def reports(self, ctx: commands.Context):
        try:
            files = [os.path.splitext(f)[0] for f in os.listdir('./reports') if f.endswith('.txt')]
            if not files:
                await ctx.send("No report found.")
                return
            await ctx.send("```\n" + "\n".join(sorted(files)) + "\n```")
        except OSError as e:
            LOG.error(f"ERROR: Failed to list reports: {e}")

    @commands.command()
    async def read(self, ctx: commands.Context, name: str):
        # Name validation
        if not self.valid_name(name):
            return await ctx.send(self.invalid_name_msg())

        # Output log file for task
        filename = f"reports/{name}.txt"
        try:
            modified = datetime.fromtimestamp(os.path.getmtime(filename), TZ)
            title = f"Report `{name}.txt`, last modified `{fmt_datetime(modified)}`\n"
            with open(filename, "r") as f:
                content = f.read()
            if not content:
                return await ctx.send(f"Report for `{name}` is empty.")
            await ctx.send(title + content)
        except FileNotFoundError:
            await ctx.send(f"ERROR: No report file found for `{name}`.")
        except OSError as e:
            LOG.error(f"Failed to read report for `{name}`: {e}")

    @commands.command()
    @commands.guild_only()
    async def poll(self, ctx: commands.Context, channel_name: str):
        channel = discord.utils.get(ctx.guild.channels, name=channel_name)

        if not channel:
            await ctx.send(f"ERROR: '{channel_name}' is not a valid channel.")
            return
        elif not isinstance(channel, discord.VoiceChannel):
            await ctx.send(f"ERROR: '{channel_name}' is not a voice channel.")
            return

        current: set[str] = {m.display_name for m in channel.members}
        lines = []

        lines.append(f"{len(current)} members in '{channel_name}' at `{fmt_now()}`")
        if current:
            lines.append("```")
            for name in current:
                lines.append(f"{name}")
            lines.append("```")

        if channel_name in self.poll_cache:
            previous = self.poll_cache[channel_name]

            # Operations allowed because of set type
            joined = current - previous
            left = previous - current

            # Members who joined the channel since the last poll
            if joined:
                lines.append("Joined since last poll")
                lines.append("```")
                for name in joined:
                    lines.append(f"{name}")
                lines.append("```")

            # Members who left the channel since the last poll
            if left:
                lines.append("Left since last poll")
                lines.append("```")
                for name in left:
                    lines.append(f"{name}")
                lines.append("```")

        # Update cache
        self.poll_cache[channel_name] = current

        await ctx.send("\n".join(lines))

    @commands.command()
    async def status(self, ctx: commands.Context):
        if not self.jobs:
            return await ctx.send("No watch tasks currently running.")

        now = datetime.now(TZ)
        lines = []

        # Check if jobs are running and if they are actually watching channels
        lines.append("Watch Status (name, created_by, status, start, end, channels)")
        lines.append("```")
        for name, (job, task) in self.jobs.items():
            lines.append(f"'{job.name}' | {job.created_by} | {job.status} | {fmt_time(job.start)} | {fmt_time(job.end)} | {self.fmt_channels(job.channel_ids)}")
        lines.append("```")

        await ctx.send("\n".join(lines))

    @commands.command()
    async def stop(self, ctx: commands.Context, name: str):
        if name not in self.jobs:
            return await ctx.send(f"No active task named `{name}` found.")

        _, task = self.jobs[name]
        task.cancel()
        await ctx.send(f"Task `{name}` has been stopped.")

