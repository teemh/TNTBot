class TNTBot(commands.Bot):
    def __init__(self):
        intents = discord.Intents.default()
        intents.members = True           # Required to see member lists
        intents.voice_states = True      # Required to see who is in voice channels
        intents.message_content = True   # Required to read command messages
        intents.messages = True          # Required to send messages
        intents.guilds = True            # Required to access guild/channel info
        super().__init__(command_prefix="!", intents=intents)

    async def setup_hook(self):
        LOG.info("AttendanceCog loaded.")
        await self.add_cog(AttendanceCog(self))

    async def dm(self, member_id: int, message: str):
        member = await self.fetch_user(member_id)
        await member.send(message)

    async def on_ready(self):
        LOG.info(f"Logged in as {self.user}")

        for command in self.commands:
            LOG.info(f"Command {command.name} registered.")

        self.guild = self.get_guild(GUILD_ID)

        if self.guild is None:
            LOG.error(f"Could not find guild with ID {GUILD_ID}.")
            return

    async def on_message(self, message):
        if message.channel.id not in ALLOWED_CHANNELS:
            return
        if message.author.id in ALLOWED_USERS:
            if message.content.startswith(self.command_prefix):
                LOG.info(f"{message.author.display_name} sent command `{message.content}`")
                await self.process_commands(message)

    async def on_command_error(self, ctx: commands.Context, error):
        # Command does not exist
        if isinstance(error, commands.CommandNotFound):
            return await ctx.send(f"Unknown command. Use `{ctx.prefix}help` to see available commands.")

        # Command missing required argument
        elif isinstance(error, commands.MissingRequiredArgument):
            return await ctx.send(f"Error: Missing Required Arugment(s)")

        # Command is on cooldown
        elif isinstance(error, commands.CommandOnCooldown):
            if ctx.author.id is self.owner_id:
                ctx.command.reset_cooldown(ctx)
                return await ctx.command.reinvoke(ctx)
            return await ctx.send(f'The command `{ctx.command}` is on cooldown {COOLDOWN_MESSAGES[error.type]} ')

        # Bot lacks permissions
        elif isinstance(error, commands.BotMissingPermissions):
            permissions = '\n'.join([f'> {permission}' for permission in error.missing_perms])
            message = f'Bot is missing the following permissions required to run the command `{ctx.command}`.\n{permissions}'
            try:
                return await ctx.send(message)
            except discord.Forbidden:
                try:
                    return await ctx.author.send(message)
                except discord.Forbidden:
                    return

        # Discord servers are having issues
        if isinstance(error.original, discord.HTTPException):
            LOG.error(f"HTTP {error.original.status} error in command: {error.original}")
            return await ctx.send(f"Discord error, please try again later.")

        raise error

    # If discord throws 503, 429 or 500 https errors, we can wait a bit and try again later
    async def safe_send(self, channel: discord.TextChannel, message: str, retries: int = 3, delay: int = 5):
        LOG.info("Using safe send.")
        for attempt in range(retries):
            try:
                return await channel.send(message)
            except discord.HTTPException as e:
                LOG.warning(f"HTTP {e.status} on attempt {attempt+1}/{retries}: {e}")
                if e.status not in {429, 500, 503} or attempt >= retries - 1:
                    LOG.error(f"Giving up on message after {attempt+1} attempts.")
                    return
                await asyncio.sleep(delay)

# Override default help command to disable it from printing command arguments by itself.
class HelpCommand(commands.DefaultHelpCommand):
    async def send_command_help(self, command):
        lines = []
        lines.append("```")
        lines.append(f"{self.context.prefix}{command.qualified_name}\n")
        lines.append(command.help or "No description given.")
        lines.append("```")
        await self.get_destination().send("\n".join(lines))

