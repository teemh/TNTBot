    @commands.command()
    @commands.guild_only()
    async def watch(self, ctx: commands.Context, name: str, start: str, duration: str, *channel_names: str, cav_only = False):
        now = datetime.now(TZ)

        # Name validation
        if not self.valid_name(name):
            return await ctx.send(self.invalid_name_msg())

        # Check if there are channels to watch
        if not channel_names:
            return await ctx.send("ERROR: You must provide at least one channel to watch.")

        # Check if job is already running
        if name in self.jobs:
            return await ctx.send(f"ERROR: task `{name}` already exists.")

        # Start time validation
        if start == "now": # lazy shortcut
            t_start = now
        else:
            try:
                t_start = datetime.strptime(start, "%H:%M")
            except (ValueError, TypeError) as e:
                return await ctx.send(f"ERROR: start time `{start}` must be in the form `hh:mm`.")

        # Duration validation NNh, Nh, NNm, Nm, NNhNNm, NhNm
        pattern = re.compile(r'^(?:(\d{1,2})h)?(?:(\d{1,2})m)?$')

        try:
            match = pattern.match(duration)
            if not match or not any(match.groups()):
                raise ValueError
            d_hours = int(match.group(1) or 0)
            d_minutes = int(match.group(2) or 0)
        except ValueError as e:
            return await ctx.send(f"ERROR: duration `{duration}` must be in the form: `XhYm`, `Xh`, or `Ym` e.g. `25h30m`, `2h`, `30m`")

        # Convert from time to datetime for the current day
        dt_start = datetime.now(TZ).replace(hour=t_start.hour, minute=t_start.minute)
        # End time is start time + duration
        dt_end = dt_start + timedelta(hours=d_hours, minutes=d_minutes)

        # Check that the time ranges make sense
        if dt_start < now:
            return await ctx.send(f"ERROR: start time `{start}` can't be in the past.")

        # Find channel ids based on names and make sure channel exists and are a voice channels.
        channels = []
        for channel_name in channel_names:
            channel = discord.utils.get(ctx.guild.channels, name=channel_name)
            if not channel:
                return await ctx.send(f"ERROR: `{channel_name}` is not a valid channel.")
            elif not isinstance(channel, discord.VoiceChannel):
                return await ctx.send(f"ERROR: `{channel_name}` is not a voice channel.")
            channels.append(channel)

        # Create a new watch job
        job = WatchJob(
            member_id = ctx.author.id,
            created_by = ctx.author.display_name,
            name=name,
            channel_ids = {c.id for c in channels},
            start = dt_start,
            end = dt_end,
            report_to = ctx.channel.id,
            cav_only = cav_only,
        )

        # Create new bot task
        task = asyncio.create_task(self.job_start(job, channels), name=f"watchjob-{name}")

        # Assign the job it's task
        self.jobs[name] = (job, task)

        await ctx.send(f"Task `{job.name}` watching {len(channels)} channel(s) from `{fmt_time(job.start)}` to `{fmt_datetime(job.end)}`.")

    @commands.command()
    async def ping(self, ctx: commands.Context, job_name: str):
        if job_name not in self.jobs:
            return await ctx.send(f"ERROR: Task `{job_name}` not found.")

        job, task = self.jobs[job_name]

        await ctx.send(f"`{job.name}` current attendance")

        report = job.build_report()
        channel = self.bot.get_channel(job.report_to)

        if channel:
            return await channel.send(report)
        else:
            LOG.error(f"Could not find report channel `{job.report_to}`.")

    # LISTENERS
    #--------------------------------------------------------------------------
    @commands.Cog.listener()
    async def on_disconnect(self):
        self.backup(interrupted = True)
        LOG.info("Bot was disconnected.")

    @commands.Cog.listener()
    async def on_resumed(self): # Called when the bot has to reconnect
        # If reconnected we have to take attendance of all the channels being watched
        # to make sure it's up to date
        LOG.info(f"Bot resumed at {fmt_now()}, taking attendance to update...")

        now = datetime.now(TZ)

        for name, (job, task) in self.jobs.items():
            # If bot was disconnect the whole time of a task window just cancel it
            if now > job.end:
                job.status = "cancelled"
                task.cancel()
                LOG.info(f"Task `{job.name} was cancelled, because bot was disconnected at time.")

            if job.status != "watching":
                continue #skip to next job

            LOG.info(f"Taking attendance on all watched channels.")

            for channel_id in job.channel_ids:
                channel = self.bot.get_channel(channel_id)
                if not channel:
                    LOG.error(f"Watch job {name} had a channel_id {channel_id} that was not a real channel.")
                    continue

                # Who is in the channel
                in_channel: set[int] = set()

                # Update members who are still in channel
                for member in channel.members:
                    in_channel.add(member.id)
                    job.record_join(channel_id, member)

                # Update members aren't in channel anymore
                for member_id, record in job.attendance[channel_id].items():
                    if record.last_joined is not None:
                        if member_id not in in_channel:
                            LOG.debug(f"`{record.display_name}` left during outage, record that they left now.")
                            record.on_leave()

    @commands.Cog.listener()
    async def on_voice_state_update(self, member: discord.Member, before: discord.VoiceState, after: discord.VoiceState):
        # If there are no channels to watch, don't do anything
        if not self.watch_list:
            return

        # Member left the channel, record their duration
        if before.channel and before.channel.id in self.watch_list:
            LOG.debug(f"{member.display_name} left channel.")
            for job in self.watch_list[before.channel.id]:
                job.record_leave(before.channel.id, member)

        # Member joined the join, record their last_joined
        if after.channel and after.channel.id in self.watch_list:
            LOG.debug(f"{member.display_name} joined channel.")
            for job in self.watch_list[after.channel.id]:
                job.record_join(after.channel.id, member)

