    @commands.command()
    @commands.guild_only()
    @commands.cooldown(rate=1, per=10, type=commands.BucketType.user)
    async def watch(self, ctx: commands.Context, name: str, start: str, duration: str, *channel_names: str, cav_only = False):
        """Schedule a new task to take attendance

        Arguments:
            name          - Unique name for the task (letters, numbers, underscores)
            start         - Start time in HH:MM format, or 'now'
            duration      - Duration in XhYm format e.g. 2h, 30m, 1h30m
            channel_names - One or more voice channel names to watch

        Example:
            !watch my_task 14:30 2h east central
        """
        now = datetime.now(TZ)

        # Name validation
        if not self.valid_name(name):
            return await ctx.send(self.invalid_name_msg())

        # Check if there are channels to watch
        if not channel_names:
            return await ctx.send("ERROR: You must provide at least one channel to watch.")

        # Check if job is already running
        if name in self.jobs:
            return await ctx.send(f"ERROR: task `{name}` already exists.")

        # Start time validation
        if start == "now": # lazy shortcut
            t_start = now
        else:
            try:
                t_start = datetime.strptime(start, "%H:%M")
            except (ValueError, TypeError) as e:
                return await ctx.send(f"ERROR: start time `{start}` must be in the form `hh:mm`.")

        # Duration validation NNh, Nh, NNm, Nm, NNhNNm, NhNm
        pattern = re.compile(r'^(?:(\d{1,2})h)?(?:(\d{1,2})m)?$')

        try:
            match = pattern.match(duration)
            if not match or not any(match.groups()):
                raise ValueError
            d_hours = int(match.group(1) or 0)
            d_minutes = int(match.group(2) or 0)
        except ValueError as e:
            return await ctx.send(f"ERROR: duration `{duration}` must be in the form: `XhYm`, `Xh`, or `Ym` e.g. `25h30m`, `2h`, `30m`")

        # Convert from time to datetime for the current day
        dt_start = datetime.now(TZ).replace(hour=t_start.hour, minute=t_start.minute, second=t_start.second)
        # End time is start time + duration
        dt_end = dt_start + timedelta(hours=d_hours, minutes=d_minutes)

        # Check that the time ranges make sense
        if dt_start < now:
            return await ctx.send(f"ERROR: start time `{start}` can't be in the past.")

        # Find channel ids based on names and make sure channel exists and are a voice channels.
        channels = []
        for channel_name in channel_names:
            channel = discord.utils.get(ctx.guild.channels, name=channel_name)
            if not channel:
                return await ctx.send(f"ERROR: `{channel_name}` is not a valid channel.")
            elif not isinstance(channel, discord.VoiceChannel):
                return await ctx.send(f"ERROR: `{channel_name}` is not a voice channel.")
            channels.append(channel)

        # Create a new watch job
        job = WatchJob(
            member_id = ctx.author.id,
            created_by = ctx.author.display_name,
            name=name,
            channel_ids = {c.id for c in channels},
            start = dt_start,
            end = dt_end,
            report_to = ctx.channel.id,
            cav_only = cav_only,
        )

        # Create new bot task
        task = asyncio.create_task(self.job_start(job, channels), name=f"watchjob-{name}")

        # Assign the job it's task
        self.jobs[name] = (job, task)

        await ctx.send(f"Task `{job.name}` watching {len(channels)} channel(s) from `{fmt_time(job.start)}` to `{fmt_datetime(job.end)}`.")

