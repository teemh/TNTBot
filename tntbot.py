    @commands.Cog.listener()
    async def on_disconnect(self): # Called when discord disconnects the bot
        for name, (job, task) in self.jobs.items():
            if job.status == "watching":
                job.disconnected_at = datetime.now(TZ)
        self.backup()
        LOG.info("Bot was disconnected. Record disconnected at times.")

    @commands.Cog.listener()
    async def on_resumed(self): # Called when the bot has to reconnect
        now = datetime.now(TZ)

        # Record the outage time
        for name, (job, task) in self.jobs.items():
            if job.disconnected_at is not None:
                job.outages.append((job.disconnected_at, now))
                job.disconnected_at = None

        # Update attendance now that the bot has reconnected.
        for name, (job, task) in self.jobs.items():
            # If bot was disconnect the whole time of a task window just cancel it
            if now > job.end:
                job.status = "cancelled"
                task.cancel()
                LOG.info(f"Task `{job.name} was cancelled, because bot was disconnected for too long.")

            if job.status != "watching":
                continue #skip to next job

            LOG.info(f"Bot resumed at {fmt_now()}")
            LOG.info(f"For task {name}, taking attendance on all their watched channels.")

            for channel_id in job.channel_ids:
                channel = self.bot.get_channel(channel_id)
                if not channel:
                    LOG.error(f"Watch job {name} had a channel_id {channel_id} that was not a real channel.")
                    continue

                # Who is in the channel
                in_channel: set[int] = set()

                # Update members who are still in channel
                for member in channel.members:
                    in_channel.add(member.id)
                    job.record_join(channel_id, member)

                # Update members aren't in channel anymore
                for member_id, record in job.attendance[channel_id].items():
                    if record.last_joined is not None:
                        if member_id not in in_channel:
                            LOG.debug(f"`{record.display_name}` left during outage, record that they left now.")
                            record.on_leave()

    @commands.Cog.listener()
    async def on_voice_state_update(self, member: discord.Member, before: discord.VoiceState, after: discord.VoiceState):
        # If there are no channels to watch, don't do anything
        if not self.watch_list:
            return

        # Member left the channel, record their duration
        if before.channel and before.channel.id in self.watch_list:
            LOG.debug(f"{member.display_name} left channel.")
            for job in self.watch_list[before.channel.id]:
                job.record_leave(before.channel.id, member)

        # Member joined the join, record their last_joined
        if after.channel and after.channel.id in self.watch_list:
            LOG.debug(f"{member.display_name} joined channel.")
            for job in self.watch_list[after.channel.id]:
                job.record_join(after.channel.id, member)

